/// The position of ScrollbarUltima on the screen.
///
/// When changing it, note that in right and left positions, the length is the height, while in top and bottom positions, the length is the width
enum ScrollbarPosition {
  /// Positioned horizontally at the top of the screen
  top,

  /// Positioned horizontally at the bottom of the screen
  bottom,

  /// Positioned vertically on the right side of the screen
  right,

  /// Positioned vertically on the left side of the screen
  left
}
